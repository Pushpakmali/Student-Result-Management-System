    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField5.getText();
        String cn = jTextField6.getText();
        String mc = jTextField7.getText();
        String dbms = jTextField8.getText();
        String os = jTextField9.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "rooT@838083&");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='" + rollno + "'");

            if (rs.next() && "B.Tech".equals(rs.getString(2))) {
                st.executeUpdate("insert into btech_result(Roll_No,Computer_Network,Mobile_Computing,DBMS,OS) values('"+rollno+"','"+cn+"','"+mc+"','"+dbms+"','"+os+"')");
                JOptionPane.showMessageDialog(null, "Result is saved successfully !");
                setVisible(false);
                new insertresult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Student is not registered OR Student has different course !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

